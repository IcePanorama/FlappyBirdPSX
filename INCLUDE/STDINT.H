#ifndef _FLAPPYBIRDPSX_STDINT_H_
#define _FLAPPYBIRDPSX_STDINT_H_

/* Signed Ints */
typedef char int8_t;
typedef short int16_t;
typedef int int32_t;

/* Unsigned Ints */
typedef unsigned char uint8_t;
typedef unsigned short uint16_t;
typedef unsigned int uint32_t;

#endif /* _FLAPPYBIRDPSX_STDINT_H_ */
